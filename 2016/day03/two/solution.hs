groupedIn :: Int -> [a] -> [[a]]
groupedIn n [] = []
groupedIn n list = [take n list] ++ rest
  where rest = groupedIn n $ drop n list

reorder :: [Triangle] -> [Triangle]
reorder triangles = flatten $ map xform $ groupedIn 3 triangles

flatten :: [[a]] -> [a]
flatten = foldr (++) []

xform :: [Triangle] -> [Triangle]
xform ((Triangle a b c) :
       (Triangle d e f) :
       (Triangle g h i) : []) = [ Triangle a d g,
                                  Triangle b e h,
                                  Triangle c f i ]

validate :: Triangle -> Bool
validate (Triangle a b c) = (a + b > c) &&
                            (a + c > b) &&
                            (b + c > a)

validateAll = length $ filter validate $ reorder triangles

main = do putStrLn "Number of Triangles"
          print validateAll
